obfuscated_string = [0x2E, 0x31, 0x30, 0x36, 0x27, 0x2C, 0x25, 0x21, 0x30, 0x31, 0x0A, 0x36, 0x27, 0x30, 0x31, 0x30, 0x3B, 0x21, 0x3C]

for key in range(0x00, 0x80):
    deobfuscated_string = ''.join(chr(char ^ key) for char in obfuscated_string)
    print(f"Key: {hex(key)} - De-obfuscated string: ISAG{deobfuscated_string}")
